#include <minishell.h>

int	ft_echo(char **s)
{
	if (s[1] == NULL)
		return (0);
	if (my_strcmp(s[1], "-n") == 0)
		aff_tab(s + 2, 2);
	else
	{
		aff_tab(s + 1, 2);
		ft_putstr("\n");
	}
	return (0);
}
